package core;

public class Array {
	private int[] array;
	private int size;

	public Array(int[] array) {
		this.array = new int[size];
		this.array = array;
	}

	public void setSize(int size) {
		this.size = array.length;
	}

	public int[] getArray() {
		return array;
	}

	public void setArray(int[] array) {
		this.array = array;

	}
}
